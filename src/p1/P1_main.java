package p1;

public class P1_main {
	public static void main(String[] args) {
		
		// while Schleife 
		int x = 0;
		while ( x <= 10) {
			x = x+1;
			System.out.println(x);
		}
		
		// do-while Schleife
		
		int y = 0;
		do { 
			y = y+1;
			System.out.println(y);
		} while (y<=10);
		
		// for Schleife
		for (int z = 1; z <= 11; z++) {
			System.out.println(z);
		}
		
	}

}
